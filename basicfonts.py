# Maintenance tool for the embedded fonts used by AVL BASIC.
#
# This file is not used at runtime by `basic.py`. Its purpose is to provide an
# editable version of the bitmap fonts so they can be adjusted by hand and then
# regenerated into the compact block copied into `basic.py`.
#
# Recommended workflow:
#
# 1. Edit the glyphs in `small_bitmap_font` and/or `big_bitmap_font` in this
#    file.
# 2. Each glyph is defined as a list of rows using only spaces and `1`.
#    - A space represents a pixel that is off.
#    - `1` represents a pixel that is on.
#    - The small font must be 8x16.
#    - The large font must be 16x16.
# 3. Generate the code ready to paste into `basic.py`:
#
#    python basicfonts.py
#
#    or, explicitly:
#
#    python -c "import basicfonts; print(basicfonts.export_fonts_for_basic_py())"
#
#    This prints the complete definitions of `small_bitmap_font` and
#    `big_bitmap_font` in the integer-tuple format used by `basic.py`.
# 4. Replace the `small_bitmap_font = (...)` and `big_bitmap_font = (...)`
#    blocks in `basic.py` with the generated output.
#
# If you only want to review or regenerate a specific character, you can use:
#
# python -c "import basicfonts; print(basicfonts.export_basic_tuples('A'))"
#
# This prints the tuples for the indicated glyph in both fonts, ready to be
# pasted manually if you prefer a one-off change.

