def _font_dimensions(font_map):
    """Return (width, height) for the provided bitmap font map."""
    if not font_map:
        raise ValueError("El mapa de la fuente está vacío.")
    sample_rows = next(iter(font_map.values()))
    if not sample_rows:
        raise ValueError("Los glifos deben contener al menos una fila.")
    width = len(sample_rows[0])
    height = len(sample_rows)
    for char, rows in font_map.items():
        if len(rows) != height:
            raise ValueError(f"El glifo {char!r} no tiene {height} filas.")
        for row in rows:
            if len(row) != width:
                raise ValueError(f"Todas las filas deben medir {width} columnas (glifo {char!r}).")
    return width, height


def _rows_to_ints(rows):
    """Convert bitmap rows (strings of ' ' and '1') into integer tuples."""
    glyph = []
    for index, row in enumerate(rows):
        invalid = set(row) - {" ", "0", "1"}
        if invalid:
            raise ValueError(f"Fila {index} contiene caracteres no válidos: {invalid}.")
        binary = row.replace(" ", "0")
        glyph.append(int(binary, 2))
    return tuple(glyph)


